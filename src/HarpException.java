package src;

public class HarpException extends Exception {

	public HarpException() {
		super();
	}

	public HarpException(String message, Throwable cause) {
		super(message, cause);
	}

	public HarpException(String message) {
		super(message);
	}

	public HarpException(Throwable cause) {
		super(cause);
	}


}
